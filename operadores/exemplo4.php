<?php
/**
 * Operadores de comparação
 */
$a = 25;
$b = 25.0;

var_dump($a > $b); // Maior que

echo "<br>";

var_dump($a < $b); // Menor que

echo "<br>";

var_dump($a == $b); // Igualdade de valor

echo "<br>";

var_dump($a === $b); // Igualdade de identidade

echo "<br>";

var_dump($a != $b); // Diferença de valor

echo "<br>";

var_dump($a !== $b); // Diferença de identidade