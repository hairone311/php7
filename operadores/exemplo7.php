<?php
/**
 * Operadores de Incremento e Decremento
 */
$a = 10;

echo $a++; // Pos-incremento

echo "<br>";

echo $a;

echo "<br>";

$b = 10;

echo ++$b; // Pre-incremento

echo "<br>";

echo $b;

echo "<br>";

$c = 10;

echo --$c; // Pre-decremento

echo "<br>";

echo $c;

echo "<br>";

$d = 10;

echo $d--; // Pos-decremento

echo "<br>";

echo $d;